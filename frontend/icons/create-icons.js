// This creates simple placeholder icons
// Run: node create-icons.js

const fs = require('fs');

// Simple 1x1 teal pixel PNG (base64)
const createSimplePNG = (size) => {
  // This is a minimal valid PNG structure
  // In production, use a proper image library
  return Buffer.from([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
    0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, // IHDR chunk
    0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, // 1x1 dimensions
    0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53,
    0xDE, 0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41,
    0x54, 0x08, 0xD7, 0x63, 0x08, 0x83, 0x95, 0x00,
    0x00, 0x01, 0x89, 0x00, 0xB9, 0x1D, 0x61, 0x9A,
    0x2F, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E,
    0x44, 0xAE, 0x42, 0x60, 0x82
  ]);
};

const sizes = [16, 32, 72, 96, 128, 144, 152, 167, 180, 192, 384, 512];

sizes.forEach(size => {
  const filename = `icon-${size}.png`;
  fs.writeFileSync(filename, createSimplePNG(size));
  console.log(`Created ${filename}`);
});

console.log('Done! Note: These are placeholder icons.');
console.log('For production, use proper icons generated from your design.');
